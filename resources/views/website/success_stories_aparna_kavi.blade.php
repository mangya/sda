            <p>This is a story of a young and charming housewife and her passion for cleanliness drive and her voyage from a housewife to an authorised Swacchata Doot of Kalyan Dombivli Municipal corporation (KDMC). The voyage still continues. This story will be an inspiration to all the citizens and specially to the house wives and it has proven that though a housewife is on duty for 24 hrs 365 days taking care of house and her family members but simultaneously she can efficiently and effectively take care of her premises, her city.  Once she decides to do this then ‘sky is the limit’ for her.</p>

            <p>Along with all these activities another strong passion was getting developed in her and that was love towards keeping the surrounding clean or ‘cleaning maniac’ that’s what her family addresses it. This passion was first channelized via the social activities conducted by Inner Wheel club. She was grown up in the family having culture like ‘Reuse and recycle plastic’, ‘save water’. And this childhood culture bounced back and dominated all her other activities thereafter. She was so much engrossed in this which led her to became the president of Inner Wheel Club in the year 2014.</p>

            <p>The Start was very effective and significant unlike the usual small Start. She started working on very micro level e.g. Once while traveling from auto rikshaw she found that the driver spitted on the road and she immediately asked him to stop the vehicle and got down from it and warned him for his misdeed of spitting. When next time found the other drivers spitting on the roads, she prohibited the other passengers from hiring those vehicles. This seems to be a small thing but the way Mrs. Aparna Kavi reacted in this situation and that too without hesitation and without thinking of public reaction is certainly a ‘to be followed’ patterns by all of passive citizens like us.</p>

            <p>While travelling by road Mrs. Aparna carries a 5lt. water can with her. If she finds burning the garbage roadside then she gets down from vehicle and finds out the person responsible for it, requests him for not doing so. If she doesn’t find anyone or if nobody listens to her then she takes out this 5lts can and extinguish the fire by pouring water on it.  HATS OFF. Isn’t it a very simple and easy way to contribute to reduce the pollution?</p>

            <p>In this voyage Mrs Kavi is working tirelessly being her own leader and being her own party worker. At the age of 59 years Mrs. Kavi learned working on computer and cleared her MSCIT examination. This is really admirable and it shows her spirit to accept and learn modern things. Learning computers was not just the sake for getting a certificate but she using this knowledge she prepared the small pamphlets containing information for her cleanliness drive like segregation of waste, recycling plastic, proper disposal of plastic, disposal of sanitary waster etc.  thus, she became a technical help for herself. These pamphlets were further used by KDMC’s sanitary department as their official document for promoting the segregation in the city with their logo printed on it. she started the segregation and disposal of domestic garbage from her own house so that she can guide others properly. She distributed the magic basket for composting the wet waste on an individual level. To introduce this concept in our city she personally got these baskets from Thane to Dombivli and after becoming an expert in this, she promoted this to her neighbours and then to her society. For disposing the garbage of broken glass material Mrs. Kavi not just asked her society member to separate the glass material but she herself kept a common bucket in which the glass garbage of all the society members can be collected and once its full Mrs. Kavi carried this to Prabhat glass factory for recycling. Thus, the initiation automatically inspired others to contribute towards this.</p>

            <p>The banner version of her waste segregation pamphlet was advertised in the auto rickshaws and KDMT buses. With continuous follow up with the KDMT authorities she was permitted to display the banners in the busses for 10 days or free of cost.</p>

            <p>All these activities run by her noticed by the KDMC authorities and in 2017 she became an official ‘Swacchata Doot’ of KDMC.</p>

            <p>Since last two years Mrs. Aparna is working on the pollution caused by the immersion of Lord Ganesh idols. With special mention of Minal Lele who makes Lord Ganesh Idols from Shadu this duo exhibits the idols of Shadu to promote them and so as to reduce the pollution caused by immersing the plaster of paris (POP) idols. The citizens are requested to return back the idols after formal immersion in the house itself and the material is recycled. For first year approximately 250 idols were returned and in the second year approximately 750 idols were returned back and this is indeed a great success. Since last year the other Shadu murtikar or the sellers of the Shadu idols were also informed to run this activity and we can imagine how a simple trick will help to reduce the pollution caused by immersing the idols to zero.</p>

